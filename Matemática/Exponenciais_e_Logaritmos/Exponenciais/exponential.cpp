#include <bits/stdc++.h>

#define pb push_back
#define mp make_pair
#define ff first
#define ss second
#define ld long double
#define ll long long
#define int long long
#define pii pair<int, int>

using namespace std;

const int MAX = 1010;

int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
  
  vector<int> power;
  power.pb(1);
  int x; cin >> x;
  for (int i = 2; i <= 1000; i++) {
    int curr = i;
    while (curr < MAX) {
      curr *= i;
      power.pb(curr);
    }
  }

  sort(power.begin(), power.end());

  auto it = upper_bound(power.begin(), power.end(), x);
  it--;

  cout << *it << '\n';

  return 0;
}
