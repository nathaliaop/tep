#define int long long

using namespace std;

long long division_ceil(long long a, long long b) {
    return 1 + ((a - 1) / b);
}

int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
  
  ll n; cin >> n;

  cout << division_ceil(n, 2) * (n % 2 == 1 ? -1 : 1) << '\n';

  return 0;
}
