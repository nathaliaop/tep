#include <bits/stdc++.h>

#define pb push_back
#define mp make_pair
#define pii pair<int, int>
#define ff first
#define ss second
#define ll long long
#define int long long

using namespace std;

int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
  
  int n, k; cin >> n >> k;
  map<int, int> left, right;
  int arr[n];

  for (int i = 0; i < n; i++) {
    cin >> arr[i];
    right[arr[i]]++;
  }

  int ans = 0;
  for (int i = 0; i < n; i++) {
    right[arr[i]]--;

    if (arr[i] % k == 0) ans += left[arr[i] / k] * right[arr[i] * k];

    left[arr[i]]++;
  }

  cout << ans << '\n';

  return 0;
}
