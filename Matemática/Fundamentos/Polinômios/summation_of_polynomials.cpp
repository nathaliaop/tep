using namespace std;

const int MAX = 1e5+10;

int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
  
  int n;
  int dp[MAX] = {};
  for (int i = 1; i < MAX; i++) {
    dp[i] = i * i * i + dp[i - 1];
  }
  while (cin >> n) {
    cout << dp[n] << '\n';
  }

  return 0;
}
