#include <bits/stdc++.h>

#define pb push_back
#define mp make_pair
#define ff first
#define ss second
#define ld long double
#define ll long long
#define int long long
#define pii pair<int, int>

using namespace std;

const int MAX = 1e5+10;

int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
  
  int n, k;
  int coef[MAX];
  while (cin >> n >> k) {
    if (n == -1 && k == -1) break;
  
    for (int i = 0; i <= n; i++) {
      cin >> coef[i];
    }

    for (int i = n; i >= k; i--) {
      coef[i - k] -= coef[i];
    }

    int val = min(k, n + 1);
    for (int i = 0; i < val; i++) {
      cout << coef[i];
      if (i != val - 1) cout << ' ';
    }

    if (val == 0) cout << 0;
    cout << '\n';
  }

  return 0;
}
