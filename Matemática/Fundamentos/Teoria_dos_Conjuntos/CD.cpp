#include <bits/stdc++.h>

#define pb push_back
#define mp make_pair
#define pii pair<int, int>
#define ff first
#define ss second
#define ll long long
#define int long long

using namespace std;

int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
  
  int n, m;
  while (cin >> n >> m) {
    if (n == 0 && m == 0) break;

    set<int> s;
    int ans = 0;

    for (int i = 0, a; i < n; i++) {
      cin >> a; s.insert(a);
    }

    for (int i = 0, a; i < n; i++) {
      cin >> a;
      if (s.find(a) != s.end())  ans++;
    }

    cout << ans << '\n';
  }

  return 0;
}
