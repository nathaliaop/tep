#include <bits/stdc++.h>

#define pb push_back
#define mp make_pair
#define pii pair<int, int>
#define ff first
#define ss second
#define ll long long
#define int long long

using namespace std;

int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
  
  int n, q; cin >> n >> q;
  vector<tuple<int, int, int>> v(n);

  for (int i = 0; i < n; i++) {
    int s, t, x; cin >> s >> t >> x;
    v[i] = make_tuple(x, s - x, t - 1 - x);
  }

  sort(v.begin(), v.end());
  multiset<pair<int, int>> ms;

  for (int i = 0; i < q; i++) {
    int d; cin >> d;
    ms.insert(mp(d, i));
  }

  map<int, int> m;

  for (auto [x, s, t] : v) {
    auto it = ms.lower_bound(mp(s, -1));

    while (it != ms.end() && (*it).ff <= t) {
      m[(*it).ss] = x;
      it = ms.erase(it);
    }
  }

  for (int i = 0; i < q; i++) {
    if (m.find(i) != m.end()) cout << m[i] << '\n';
    else cout << "-1\n";
  }

  return 0;
}
