#include <bits/stdc++.h>

#define pb push_back
#define mp make_pair
#define pii pair<int, int>
#define ff first
#define ss second
#define ll long long
#define int long long

using namespace std;

int solve(int n, int k) {
  if (n < k) return 0;
  return n / k + solve(n / k + n % k, k);
}

int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
  
  int n, k;
  while (cin >> n >> k) {
    cout << n + solve(n, k) << '\n';
  }

  return 0;
}
