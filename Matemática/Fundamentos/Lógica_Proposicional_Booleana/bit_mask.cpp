#include <bits/stdc++.h>

#define pb push_back
#define mp make_pair
#define pii pair<int, int>
#define ff first
#define ss second
#define ll long long
#define int long long

using namespace std;

int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
  
  int n, l, u;
  while (cin >> n >> l >> u) {
    int ans = 0;
    for (int i = 31; i >= 0; i--) {
      int tmp = ans | (1LL << i);
      if (n & (1LL << i)) {
        if (tmp <= l) ans |= (1LL << i);
      } else {
        if (tmp <= u) ans |= (1LL << i);
      }
    }

    cout << ans << '\n';
  } 

  return 0;
}
