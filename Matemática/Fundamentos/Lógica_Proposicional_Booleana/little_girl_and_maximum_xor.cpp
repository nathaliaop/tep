#include <bits/stdc++.h>

#define pb push_back
#define mp make_pair
#define pii pair<int, int>
#define ff first
#define ss second
#define ll long long
#define int long long

using namespace std;

int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
  
  int l, r; cin >> l >> r;

  int pos = -1;
  for (int i = 62; i >= 0; i--) {
    bool l_on = l & (1LL << i);
    bool r_on = r & (1LL << i);
    if ((l_on ^ r_on) == 1) {
      pos = i;
      break;
    }
  }

  int ans = 0;

  for (int i = pos; i >= 0; i--) {
    ans |= (1LL << i);
  }

  cout << ans << '\n';

  return 0;
}
