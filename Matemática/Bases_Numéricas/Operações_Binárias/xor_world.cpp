// 1    01  1
// 2    11  n + 1
// 3    00  0
// 4   100  n

int get_xor (int x) {
  switch (x % 4) {
    case 0:
      return x;
      break;
    case 1:
      return 1;
      break;
    case 2:
      return x + 1;
      break;
    case 3:
      return 0;
      break;
  }
  return 0;
}

int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
  
  int a, b; cin >> a >> b;
  
  // all elements less than appear twice in the xor
  cout << (get_xor(a - 1) ^ get_xor(b)) << '\n';

  return 0;
}
