int32_t main() {
  ios::sync_with_stdio(false);
  cin.tie(NULL);
 
  /*
  2
  n 1
  n 3

  8       100 0  100 1     11 1
  998     1001 1001 1  000

  1000 1001

  4

  4 4 4

  (4 * n - n ) / 4 => 9
  ceil(n / 4) => 8
  */

  int t; cin >> t;
  while (t--) {
    int n; cin >> n;
    int nine = (3 * n) / 4;
    int eight = n / 4 + (n % 4 == 0 ? 0 : 1);

    for (int i = 0; i < nine; i++) cout << "9";
    for (int i = 0; i < eight; i++) cout << "8";
    cout << '\n';
  }

  return 0;
}
